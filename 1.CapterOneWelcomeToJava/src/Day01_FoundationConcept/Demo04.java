package Day01_FoundationConcept;

public class Demo04 {
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);              //A

        int num = c;
        System.out.println(num);            //65

        num += 32;
        System.out.println((char) num);     //a
    }
}
