package Day02_ProgramLogical;

public class Demo03Switch {
    public static void main(String[] args) {
        int ch = 1;
        switch (ch){
            case 1 :
                System.out.println("第一种情况");
                break;
            case 2 :
                System.out.println("第二种情况");
                break;
            default :
                System.out.println("默认情况");
        }
    }
}
